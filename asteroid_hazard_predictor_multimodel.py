import gradio as gr
import joblib
import numpy as np
import pandas as pd

best_model_obj = joblib.load('best_model.pkl')
scaler         = joblib.load('scaler.pkl')
feature_names  = joblib.load('feature_names.pkl')
model_meta     = joblib.load('model_meta.pkl')

# Build lookup table (keep Neo Reference ID for lookup, drop other non-predictive cols)
raw = pd.read_csv('/content/nasa.csv')
raw['Hazardous'] = raw['Hazardous'].astype(int)

drop_cols = [
    'Name', 'Orbit ID', 'Close Approach Date',
    'Epoch Date Close Approach', 'Orbit Determination Date',
    'Orbiting Body', 'Equinox',
    'Est Dia in KM(max)',
    'Est Dia in M(min)', 'Est Dia in M(max)',
    'Est Dia in Miles(min)', 'Est Dia in Miles(max)',
    'Est Dia in Feet(min)', 'Est Dia in Feet(max)',
    'Relative Velocity km per hr', 'Miles per hour',
    'Miss Dist.(lunar)', 'Miss Dist.(kilometers)', 'Miss Dist.(miles)'
]
raw = raw.drop(drop_cols, axis=1, errors='ignore')
raw['Neo Reference ID'] = raw['Neo Reference ID'].astype(str).str.strip()
lookup_df = raw.set_index('Neo Reference ID')

sample_ids = raw['Neo Reference ID'].head(8).tolist()

def predict_by_id(asteroid_id):
    asteroid_id = str(asteroid_id).strip()

    if asteroid_id not in lookup_df.index:
        return (
            f"Asteroid ID '{asteroid_id}' not found.\n"
            f"Try one of these: {', '.join(sample_ids[:5])}"
        )

    row       = lookup_df.loc[asteroid_id]
    actual    = int(row['Hazardous'])
    features  = row.drop('Hazardous')[feature_names]
    input_arr = np.array([features.values], dtype=float)

    if model_meta['scaled']:
        input_arr = scaler.transform(input_arr)

    pred      = best_model_obj.predict(input_arr)[0]
    prob      = best_model_obj.predict_proba(input_arr)[0]
    haz_prob  = prob[1] * 100
    safe_prob = prob[0] * 100

    pred_str   = 'HAZARDOUS'   if pred   == 1 else 'NON-HAZARDOUS'
    actual_str = 'HAZARDOUS'   if actual == 1 else 'NON-HAZARDOUS'
    verdict    = 'CORRECT ✓'  if pred == actual else 'INCORRECT ✗'

    lines = [
        f'Asteroid ID   : {asteroid_id}',
        f'{"─" * 38}',
        f'Prediction    : {pred_str}',
        f'Actual Label  : {actual_str}',
        f'Verdict       : {verdict}',
        f'{"─" * 38}',
        f'Hazard Prob   : {haz_prob:.1f}%',
        f'Safe Prob     : {safe_prob:.1f}%',
        f'{"─" * 38}',
        f'Model Used    : {model_meta["name"]}',
    ]
    return '\n'.join(lines)

interface = gr.Interface(
    fn=predict_by_id,
    inputs=gr.Textbox(
        label='Neo Reference ID',
        placeholder='e.g. 2000433',
        lines=1
    ),
    outputs=gr.Textbox(label='Result', lines=12),
    title='Asteroid Hazard Predictor',
    description=(
        'Enter a NASA Neo Reference ID to check if the asteroid is hazardous. '
        f'| Model: {model_meta["name"]}'
    ),
    examples=[[sid] for sid in sample_ids],
    theme=gr.themes.Soft()
)

interface.launch(share=True)